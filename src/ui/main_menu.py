"""Модуль главного меню приложения."""

class MainMenu:
    """Класс главного меню шахматного приложения.

    Attributes:
        title (str): Название главного меню.
    """

    def __init__(self):
        """Инициализирует главное меню."""
        self.title = "Главное меню"

    def open_game_mode(self):
        """Открывает раздел выбора игрового режима.

        Returns:
            str: Сообщение об открытии раздела.
        """
        return "Открыт выбор режима игры"

    def open_statistics(self):
        """Открывает раздел статистики игрока.

        Returns:
            str: Сообщение об открытии статистики.
        """
        return "Открыт раздел статистики"
