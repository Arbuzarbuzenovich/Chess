"""Модуль отображения шахматной доски."""

class BoardView:
    """Класс визуального представления шахматной доски.

    Attributes:
        board_size (int): Размер шахматной доски.
    """

    def __init__(self, board_size=8):
        """Инициализирует представление доски.

        Args:
            board_size (int): Размер стороны доски.
        """
        self.board_size = board_size

    def render(self):
        """Отображает шахматную доску на экране.

        Returns:
            str: Сообщение об отображении доски.
        """
        return "Шахматная доска отображена"

    def clear(self):
        """Очищает текущее представление доски.

        Returns:
            str: Сообщение об очистке доски.
        """
        return "Доска очищена"
